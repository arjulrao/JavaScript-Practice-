/* First Syntax --> Function Declaration */
function sayHi() {
    console.log("Hii");
}
// Function call --> 
sayHi();
// We can give Parameters and Arguments.
/* Second Syntax --> Function Expressions */
let sayHi_1 = function(){
    console.log("Hey");
}
sayHi_1();

/* Note --> There is no name need after function keyword. */
